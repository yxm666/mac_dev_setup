#!/bin/bash
# ============================================
# macOS Dev Environment Setup Script
# Usage:
#   ./setup-mac-dev.sh terminal    - Fish + Starship + Ghostty
#   ./setup-mac-dev.sh vscode      - VS Code settings + extensions
#   ./setup-mac-dev.sh all         - Everything (default)
# ============================================

set -e

MODE="${1:-all}"
TERMINAL_SUMMARY=()

add_terminal_summary() {
    local item="$1"
    TERMINAL_SUMMARY+=("$item")
}

print_terminal_summary() {
    local i=1
    local item
    if [[ "${#TERMINAL_SUMMARY[@]}" -eq 0 ]]; then
        return
    fi

    echo ""
    echo "📋 Terminal changes summary:"
    for item in "${TERMINAL_SUMMARY[@]}"; do
        echo "  $i. $item"
        i=$((i + 1))
    done
}

print_terminal_next_steps() {
    echo ""
    echo "🧭 Next steps:"
    echo "  1. Quit and reopen Ghostty to apply glass blur and theme updates."
    echo "  2. Open a new terminal tab and verify Starship powerline symbols render correctly."
    echo "  3. If the default shell is still not fish, run: chsh -s \"$(resolve_bin fish)\""
    echo "  4. If prompt looks wrong, run: exec fish"
}

ensure_formula_installed() {
    local formula="$1"
    if brew list --formula "$formula" &> /dev/null; then
        echo "✅ $formula already installed (skip)"
        add_terminal_summary "brew formula already present: $formula (idempotent skip)"
        return
    fi

    echo "📦 Installing $formula..."
    brew install "$formula"
    add_terminal_summary "installed brew formula: $formula"
}

ensure_cask_installed() {
    local cask="$1"
    if brew list --cask "$cask" &> /dev/null; then
        echo "✅ $cask already installed (skip)"
        add_terminal_summary "brew cask already present: $cask (idempotent skip)"
        return
    fi

    echo "📦 Installing $cask..."
    brew install --cask "$cask"
    add_terminal_summary "installed brew cask: $cask"
}

backup_file() {
    local target="$1"
    if [[ -f "$target" ]]; then
        local backup="${target}.bak.$(date +%Y%m%d%H%M%S)"
        cp "$target" "$backup"
        echo "🗂️  Backed up $target -> $backup"
        add_terminal_summary "backup created: $target -> $backup"
    fi
}

init_brew_shellenv() {
    local brew_bin=""
    if [[ -x /opt/homebrew/bin/brew ]]; then
        brew_bin="/opt/homebrew/bin/brew"
    elif [[ -x /usr/local/bin/brew ]]; then
        brew_bin="/usr/local/bin/brew"
    elif command -v brew &> /dev/null; then
        brew_bin="$(command -v brew)"
    fi

    if [[ -n "$brew_bin" ]]; then
        eval "$("$brew_bin" shellenv)"
        return 0
    fi

    return 1
}

resolve_bin() {
    local bin_name="$1"

    if command -v "$bin_name" &> /dev/null; then
        command -v "$bin_name"
        return 0
    fi

    if [[ -x "/opt/homebrew/bin/$bin_name" ]]; then
        echo "/opt/homebrew/bin/$bin_name"
        return 0
    fi

    if [[ -x "/usr/local/bin/$bin_name" ]]; then
        echo "/usr/local/bin/$bin_name"
        return 0
    fi

    return 1
}

current_login_shell() {
    dscacheutil -q user -a name "$USER" 2>/dev/null | awk '/^shell:/ {print $2; exit}'
}

strip_outer_quotes() {
    local s="$1"
    local len=${#s}
    local first_char last_char

    if [[ "$len" -lt 2 ]]; then
        printf '%s' "$s"
        return
    fi

    first_char="${s:0:1}"
    last_char="${s:$((len - 1)):1}"
    if [[ "$first_char" == "$last_char" ]] && [[ "$first_char" == "\"" || "$first_char" == "'" ]]; then
        printf '%s' "${s:1:$((len - 2))}"
        return
    fi

    printf '%s' "$s"
}

escape_for_double_quotes() {
    local s="$1"
    s="${s//\\/\\\\}"
    s="${s//\"/\\\"}"
    printf '%s' "$s"
}

migrate_zsh_to_fish() {
    local zsh_files out_file alias_tmp_file
    local file line trimmed entry key val raw
    local found_zsh_file=0
    local migrated_exports=0
    local migrated_paths=0
    local migrated_aliases=0
    local skipped_items=0

    zsh_files=("$HOME/.zshenv" "$HOME/.zprofile" "$HOME/.zshrc")
    out_file="$HOME/.config/fish/conf.d/90-zsh-migration.fish"

    for file in "${zsh_files[@]}"; do
        if [[ -f "$file" ]]; then
            found_zsh_file=1
            break
        fi
    done

    if [[ "$found_zsh_file" -eq 0 ]]; then
        echo "ℹ️  No zsh config files found, skipping zsh -> fish migration."
        return
    fi

    echo "🔄 Migrating zsh config to fish (best effort)..."
    mkdir -p "$HOME/.config/fish/conf.d"
    backup_file "$out_file"

    alias_tmp_file="$(mktemp)"
    {
        echo "# Auto-generated by setup-mac-dev.sh on $(date '+%Y-%m-%d %H:%M:%S')"
        echo "# Source: ~/.zshenv ~/.zprofile ~/.zshrc"
        echo "# This file migrates common exports / PATH / aliases from zsh to fish."
        echo "# Manual review recommended for plugin/framework-specific config."
    } > "$out_file"

    for file in "${zsh_files[@]}"; do
        [[ -f "$file" ]] || continue

        echo "" >> "$out_file"
        echo "# ---- from $file ----" >> "$out_file"

        while IFS= read -r line || [[ -n "$line" ]]; do
            trimmed="$(printf '%s\n' "$line" | sed -E 's/^[[:space:]]+//; s/[[:space:]]+$//')"
            [[ -z "$trimmed" || "$trimmed" == \#* ]] && continue

            if [[ "$trimmed" =~ ^export[[:space:]]+([A-Za-z_][A-Za-z0-9_]*)=(.*)$ ]]; then
                key="${BASH_REMATCH[1]}"
                val="${BASH_REMATCH[2]}"
                raw="$(strip_outer_quotes "$val")"

                if [[ "$key" == "PATH" ]]; then
                    local tokens token
                    IFS=':' read -r -a tokens <<< "$raw"
                    for token in "${tokens[@]}"; do
                        token="$(printf '%s\n' "$token" | sed -E 's/^[[:space:]]+//; s/[[:space:]]+$//')"
                        token="$(strip_outer_quotes "$token")"
                        if [[ -z "$token" || "$token" == *'$PATH'* || "$token" == *'${PATH}'* ]]; then
                            continue
                        fi
                        token="$(escape_for_double_quotes "$token")"
                        echo "fish_add_path -m \"$token\"" >> "$out_file"
                        migrated_paths=$((migrated_paths + 1))
                    done
                    continue
                fi

                if [[ "$raw" == *'$('* || "$raw" == *'`'* ]]; then
                    skipped_items=$((skipped_items + 1))
                    continue
                fi

                raw="$(escape_for_double_quotes "$raw")"
                echo "set -gx $key \"$raw\"" >> "$out_file"
                migrated_exports=$((migrated_exports + 1))
                continue
            fi

            if [[ "$trimmed" =~ ^alias[[:space:]]+([A-Za-z_][A-Za-z0-9_]*)=(.*)$ ]]; then
                key="${BASH_REMATCH[1]}"
                val="${BASH_REMATCH[2]}"
                raw="$(strip_outer_quotes "$val")"
                if [[ "$raw" == *'$('* || "$raw" == *'`'* ]]; then
                    skipped_items=$((skipped_items + 1))
                    continue
                fi
                raw="$(escape_for_double_quotes "$raw")"
                echo "    alias $key \"$raw\"" >> "$alias_tmp_file"
                migrated_aliases=$((migrated_aliases + 1))
            fi
        done < "$file"
    done

    if [[ "$migrated_aliases" -gt 0 ]]; then
        {
            echo ""
            echo "if status is-interactive"
            cat "$alias_tmp_file"
            echo "end"
        } >> "$out_file"
    fi

    rm -f "$alias_tmp_file"

    if [[ "$migrated_exports" -eq 0 && "$migrated_paths" -eq 0 && "$migrated_aliases" -eq 0 ]]; then
        echo "" >> "$out_file"
        echo "# No simple export/PATH/alias items were migrated from zsh files." >> "$out_file"
    fi

    echo "✅ zsh migration file written: $out_file"
    echo "   exports: $migrated_exports, paths: $migrated_paths, aliases: $migrated_aliases, skipped: $skipped_items"
    add_terminal_summary "migrated zsh -> fish to $out_file (exports=$migrated_exports, paths=$migrated_paths, aliases=$migrated_aliases, skipped=$skipped_items)"
}

print_terminal_status() {
    local login_shell fish_path starship_path ghostty_theme
    local has_catppuccin_powerline="no"

    login_shell="$(current_login_shell)"
    fish_path="$(resolve_bin fish || true)"
    starship_path="$(resolve_bin starship || true)"
    ghostty_theme="$(sed -n 's/^theme[[:space:]]*=[[:space:]]*//p' "$HOME/.config/ghostty/config" 2>/dev/null | head -n1)"

    if [[ -n "$starship_path" ]] && "$starship_path" preset --list 2>/dev/null | grep -qx 'catppuccin-powerline'; then
        has_catppuccin_powerline="yes"
    fi

    echo ""
    echo "🔎 Terminal status:"
    echo "   Login shell: ${login_shell:-unknown}"
    echo "   Fish binary: ${fish_path:-not found}"
    echo "   Starship binary: ${starship_path:-not found}"
    echo "   Starship preset catppuccin-powerline: $has_catppuccin_powerline"
    echo "   Ghostty theme: ${ghostty_theme:-not set}"
}

usage() {
    echo "Usage: $0 [terminal|vscode|all]"
    echo ""
    echo "  terminal  - Install & configure Fish, Starship, Ghostty"
    echo "  vscode    - Configure VS Code settings & install extensions"
    echo "  all       - Run both (default)"
    exit 1
}

if [[ "$MODE" != "terminal" && "$MODE" != "vscode" && "$MODE" != "all" ]]; then
    usage
fi

# ============================================
# Shared: Homebrew & Font
# ============================================
setup_shared() {
    # Homebrew
    if ! command -v brew &> /dev/null; then
        echo "📦 Installing Homebrew..."
        /bin/bash -c "$(curl -fsSL https://raw.githubusercontent.com/Homebrew/install/HEAD/install.sh)"
        if ! init_brew_shellenv; then
            echo "❌ Homebrew installed but brew command not found in expected paths."
            exit 1
        fi
    else
        echo "✅ Homebrew already installed"
        init_brew_shellenv || true
    fi

    # Font
    echo "🔤 Installing JetBrainsMono Nerd Font..."
    if brew list --cask font-jetbrains-mono-nerd-font &> /dev/null; then
        echo "✅ Font already installed"
    else
        brew install --cask font-jetbrains-mono-nerd-font
    fi
}

