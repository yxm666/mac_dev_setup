setup_terminal() {
    echo ""
    echo "🖥️  Setting up terminal environment..."
    TERMINAL_SUMMARY=()

    # Install packages
    echo "📦 Checking Fish, Starship, Ghostty..."
    ensure_formula_installed fish
    ensure_formula_installed starship
    ensure_cask_installed ghostty
    init_brew_shellenv || true

    # Set Fish as default shell
    FISH_PATH="$(resolve_bin fish || true)"
    LOGIN_SHELL="$(current_login_shell)"
    if [[ -z "$FISH_PATH" ]]; then
        echo "❌ Fish binary not found after install."
        exit 1
    fi

    if [[ "$LOGIN_SHELL" != "$FISH_PATH" ]]; then
        echo "🐟 Setting Fish as default shell..."
        if grep -qx "$FISH_PATH" /etc/shells; then
            if chsh -s "$FISH_PATH"; then
                echo "✅ Fish set as default shell"
                add_terminal_summary "set login shell to fish: $FISH_PATH"
            else
                echo "⚠️  Failed to set Fish as default shell. Run manually: chsh -s \"$FISH_PATH\""
                add_terminal_summary "could not set login shell automatically; manual action needed: chsh -s \"$FISH_PATH\""
            fi
        else
            echo "⚠️  Fish path not in /etc/shells: $FISH_PATH"
            echo "   Run manually:"
            echo "   sudo sh -c 'printf \"%s\\n\" \"$FISH_PATH\" >> /etc/shells' && chsh -s \"$FISH_PATH\""
            add_terminal_summary "fish path missing in /etc/shells; manual action needed to switch shell"
        fi
    else
        echo "✅ Fish is already default shell"
        add_terminal_summary "login shell already fish (idempotent skip)"
    fi

    # Configure Fish
    echo "🐟 Configuring Fish..."
    mkdir -p ~/.config/fish
    backup_file ~/.config/fish/config.fish
    cat > ~/.config/fish/config.fish << 'FISH_EOF'
